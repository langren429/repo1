void transfer_data_lcd(int data1)
{
	char i;
	RS_H;
	for(i=0;i<8;i++)
   {
		SCK_L;
		if(data1&0x80) 
			SDA_H;
		else 
			SDA_L;
//		delay_us(50);
		SCK_H;
//		delay_us(50);
	 	data1 <<= 1;
    }
}

//LCD模块初始化
void initial_lcd()
{
		CS_L;
		ROM_CS_H;
		RESET_L;        //低电平复位
		osDelay(80);
		RESET_H;		    //复位完毕
		osDelay(20);        
		transfer_command_lcd(0xe2);	 //软复位
		osDelay(5);
		transfer_command_lcd(0x2c);  //升压步骤1
		osDelay(5);	
		transfer_command_lcd(0x2e);  //升压步骤2
		osDelay(5);
		transfer_command_lcd(0x2f);  //升压步骤3
		osDelay(5);
		transfer_command_lcd(0x23);  //粗调对比度,可设置范围0x20~0x27
		transfer_command_lcd(0x81);  //微调对比度
		transfer_command_lcd(0x1C);  //0x1a,微调对比度的值,可设置范围0x00~0x3f
		transfer_command_lcd(0xa2);  //1/9偏压比(bias)
		transfer_command_lcd(0xc8);  //行扫描顺序:从上到下
		transfer_command_lcd(0xa0);  //列扫描顺序:从左到右
		transfer_command_lcd(0x40);  //起始行:从第一行开始
		transfer_command_lcd(0xaf);  //开显示
		CS_H;
}

