//显示16x16点阵图像、汉字、生僻字或16x16点阵的其他图标
void display_graphic_16x16(int page,int column,char reverse,char *dp)
{
	int i,j;
	char page_address;
	char column_address_L,column_address_H;

	page_address = 0xb0+page-1;
	column_address_L =column&0x0f;
	column_address_H =((column>>4)&0x0f)+0x10;
 	CS_L;
	ROM_CS_H; 	

	for(j=0;j<2;j++)
	{
		transfer_command_lcd(page_address+j);   //设置页地址
		transfer_command_lcd(column_address_H);	//设置列地址的高4位
		transfer_command_lcd(column_address_L);	//设置列地址的低4位
		for (i=0;i<16;i++)
		{	
			if(reverse == 1)
			{
				transfer_data_lcd(~*dp);
			}
			else
			{
					transfer_data_lcd(*dp);					//写数据到LCD,每写完一个8位的数据后列地址自动加1
			}

			dp++;
		}
	}
	CS_H;
}

