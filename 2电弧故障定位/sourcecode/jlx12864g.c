//写指令到LCD模块
void transfer_command_lcd(int data1)   
{
	char i;
	RS_L;
	for(i=0;i<8;i++)
   {
		SCK_L;
		if(data1&0x80) 
		  SDA_H;
		else 
			SDA_L;
	//	delay_us(50);
		SCK_H;
	//	delay_us(50);
	 	data1 <<= 1;
   }
}

